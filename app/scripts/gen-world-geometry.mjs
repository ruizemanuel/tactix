// Dev-only. Bakes app/lib/game/worldGeometry.ts from Natural Earth (world-atlas).
// Each territory = the merged outline of its member countries (ISO 3166-1 numeric ids).
import { writeFileSync } from "node:fs";
import { createRequire } from "node:module";
import { feature, merge } from "topojson-client";
import { geoNaturalEarth1, geoPath } from "d3-geo";

const require = createRequire(import.meta.url);
const topo = require("world-atlas/countries-110m.json");
const countries = topo.objects.countries;

// territory id -> member country numeric ids (strings, ISO 3166-1 numeric, zero-padded to 3 digits as used in world-atlas).
// Note: some very small countries (e.g. Bahrain 048) are absent from the 110m dataset and are omitted.
const MEMBERS = {
  greenland: ["304"], canada: ["124"], usa: ["840"], mexico: ["484"],
  "central-america": ["320", "340", "558", "188", "591", "084", "222"],
  caribbean: ["192", "214", "332", "388"],
  colombia: ["170", "862", "218"], brazil: ["076"], peru: ["604", "068"],
  argentina: ["032", "152", "858", "600"],
  britain: ["826", "372", "352"], scandinavia: ["578", "752", "246", "208"],
  "west-europe": ["250", "724", "620", "056", "528"],
  "central-europe": ["276", "616", "380", "040", "203", "756"],
  "east-europe": ["804", "112", "642", "498", "348", "703"],
  "north-africa": ["504", "012", "434", "788", "478", "466", "562"],
  egypt: ["818", "729"], congo: ["180", "178", "120", "266"],
  "east-africa": ["404", "231", "834", "800", "646"],
  "south-africa": ["710", "516", "072", "894", "716"],
  "middle-east": ["682", "364", "368", "792", "760", "887", "784", "414", "634", "512"],
  india: ["356", "586", "050", "144"], "central-asia": ["398", "860", "795", "417", "762", "004"],
  russia: ["643"], china: ["156", "158"], mongolia: ["496"], japan: ["392", "408", "410"],
  "southeast-asia": ["764", "704", "104", "458", "116", "418"],
  indonesia: ["360"], "new-guinea": ["598"], australia: ["036"], "new-zealand": ["554"],
};

const projection = geoNaturalEarth1().fitExtent([[12, 12], [988, 548]], feature(topo, countries));
const path = geoPath(projection);
const byId = new Map(countries.geometries.map((g) => [String(g.id), g]));

const out = {};
const empties = [];
for (const [tid, ids] of Object.entries(MEMBERS)) {
  const geoms = ids.map((id) => byId.get(id)).filter(Boolean);
  if (geoms.length === 0) { empties.push(tid); continue; }
  const merged = merge(topo, geoms);
  const d = path(merged);
  const c = path.centroid(merged);
  out[tid] = { path: d, labelAt: { x: Math.round(c[0] * 10) / 10, y: Math.round(c[1] * 10) / 10 } };
}
if (empties.length) console.warn("! territories with NO geometry (fix their ISO codes):", empties.join(", "));

const header = `// GENERATED by scripts/gen-world-geometry.mjs — do not edit by hand.\n// Smooth territory outlines (viewBox 0 0 1000 560) baked from Natural Earth (world-atlas, public domain).\n`;
const body = `export interface TerritoryGeometry { path: string; labelAt: { x: number; y: number } }\n\nexport const WORLD_GEOMETRY: Record<string, TerritoryGeometry> = ${JSON.stringify(out, null, 2)};\n`;
writeFileSync(new URL("../lib/game/worldGeometry.ts", import.meta.url), header + "\n" + body);
console.log(`Wrote ${Object.keys(out).length}/32 territory geometries.`);
